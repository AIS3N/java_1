package poo.exo1;

public class CDI extends Salarie {
    int sb;

    public CDI(String nom, String prenom, int sb) {
        super(nom, prenom);
        this.sb = sb;
    }

    public int getSb() {
        return sb;
    }

    public void setSb(int sb) {
        this.sb = sb;
    }

    public int getSalary() {
        return sb;
    }
}
